function headerJsx(bp: WebsiteBlueprint): string {
  const { header } = bp.ux;
  const bookingHref = extractBookingUrl(bp.business.booking) ?? `tel:${bp.business.phone.replace(/\D/g, "")}`;
  return `<Header
        businessName={businessName}
        navigation={navigation}
        showPhone={${header.showPhone}}
        phone={contact.phone}
        showCTA={${header.showCTA}}
        ctaLabel="${escapeTemplate(header.ctaLabel)}"
        ctaHref="${bookingHref}"
      />`;
}

