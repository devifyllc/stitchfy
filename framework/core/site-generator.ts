function generateSitemapXml(bp: WebsiteBlueprint): string {
  const today = new Date().toISOString().split("T")[0];
  const urls = bp.pages
    .map((p) => `  <url><loc>${p.path}</loc><lastmod>${today}</lastmod><priority>${p.path === "/" ? "1.0" : "0.8"}</priority></url>`)
    .join("\n");

  return `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${urls}
</urlset>
`;
}

