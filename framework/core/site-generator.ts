function buildJsonLd(business: BusinessData, ux: WebsiteBlueprint["ux"]): object {
  const addressParts = business.address.match(/^(.+),\s*(.+),\s*([A-Z]{2})\s+(\d{5})/);
  const streetAddress = addressParts ? addressParts[1].trim() : business.address;
  const locality = addressParts ? addressParts[2].trim() : business.location.split(",")[0];
  const region = addressParts ? addressParts[3] : "TX";
  const postalCode = addressParts ? addressParts[4] : "";

  return {
    "@context": "https://schema.org",
    "@type": "BeautySalon",
    name: business.name,
    description: business.description,
    telephone: business.phone,
    email: business.email,
    address: {
      "@type": "PostalAddress",
      streetAddress,
      addressLocality: locality,
      addressRegion: region,
      postalCode,
      addressCountry: "US",
    },
    openingHoursSpecification: buildOpeningHoursSpec(business.operatingHours),
    sameAs: Object.values(business.socialLinks),
    image: "/og-image.svg",
    priceRange: "$$",
    servesCuisine: undefined,
  };
}

function buildOpeningHoursSpec(hours: Record<string, string>): object[] {
  const DAY_MAP: Record<string, string[]> = {
    "Monday": ["Monday"],
    "Tuesday": ["Tuesday"],
    "Wednesday": ["Wednesday"],
    "Thursday": ["Thursday"],
    "Friday": ["Friday"],
    "Saturday": ["Saturday"],
    "Sunday": ["Sunday"],
    "Monday–Friday": ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday"],
    "Monday-Friday": ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday"],
    "Weekdays": ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday"],
  };

  const specs: object[] = [];
  for (const [dayLabel, timeRange] of Object.entries(hours)) {
    if (dayLabel === "Note") continue;
    const days = DAY_MAP[dayLabel];
    if (!days) continue;
    const m = timeRange.match(/(\d+:\d+)\s*(AM|PM)\s*[–\-]\s*(\d+:\d+)\s*(AM|PM)/i);
    if (!m) continue;
    const opens = to24h(m[1], m[2]);
    const closes = to24h(m[3], m[4]);
    for (const day of days) {
      specs.push({ "@type": "OpeningHoursSpecification", dayOfWeek: `https://schema.org/${day}`, opens, closes });
    }
  }
  return specs;
}

function to24h(time: string, ampm: string): string {
  const [h, m] = time.split(":").map(Number);
  let hour = h;
  if (ampm.toUpperCase() === "PM" && hour !== 12) hour += 12;
  if (ampm.toUpperCase() === "AM" && hour === 12) hour = 0;
  return `${String(hour).padStart(2, "0")}:${String(m).padStart(2, "0")}`;
}

function toPascalCase(str: string): string {
  return str.replace(/([-_][a-z])/gi, (s) => s.toUpperCase().replace(/[-_]/, "")).replace(/^./, (s) => s.toUpperCase());
}

function escapeTemplate(s: string): string {
  return s.replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}
