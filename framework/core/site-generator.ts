/**
 * site-generator — Generates a self-contained Next.js app from a blueprint.
 *
 * Output: output/generated-site/
 * Components are copied from site-template/; pages are generated with real data embedded.
 * After generation, run `next build` inside output/generated-site/ to produce
 * static HTML in output/generated-site/out/, then copy to output/static-site/.
 */

import * as fs from "fs";
import * as path from "path";
import type { WebsiteBlueprint, BusinessData } from "../schemas/blueprint.types.js";

export interface GeneratorResult {
  ok: boolean;
  generatedDir: string;
  filesWritten: number;
  warnings: string[];
}

// ─── Entry point ──────────────────────────────────────────────────────────────

export function generateSite(blueprint: WebsiteBlueprint, projectRoot: string): GeneratorResult {
  const generatedDir = path.join(projectRoot, "output", "generated-site");
  const templateDir = path.join(projectRoot, "site-template");
  const warnings: string[] = [];
  let filesWritten = 0;

  const write = (rel: string, content: string) => {
    fs.mkdirSync(path.dirname(path.join(generatedDir, rel)), { recursive: true });
    fs.writeFileSync(path.join(generatedDir, rel), content, "utf-8");
    filesWritten++;
  };

  const copyFile = (fromRel: string, toRel: string) => {
    const src = path.join(templateDir, fromRel);
    const dst = path.join(generatedDir, toRel);
    if (fs.existsSync(src)) {
      fs.mkdirSync(path.dirname(dst), { recursive: true });
      fs.copyFileSync(src, dst);
      filesWritten++;
    } else {
      warnings.push(`Template file not found: ${fromRel}`);
    }
  };

  // ── Directory scaffold ──────────────────────────────────────────────────────
  fs.mkdirSync(generatedDir, { recursive: true });

  // ── Package config ──────────────────────────────────────────────────────────
  write("package.json", JSON.stringify({ name: "generated-site", version: "1.0.0", private: true }, null, 2));

  write("next.config.js", `/** @type {import('next').NextConfig} */
const nextConfig = { output: "export", trailingSlash: true, images: { unoptimized: true } };
module.exports = nextConfig;
`);

  write("tsconfig.json", JSON.stringify({
    compilerOptions: {
      lib: ["dom", "dom.iterable", "esnext"],
      allowJs: true, skipLibCheck: true, strict: true, noEmit: true,
      esModuleInterop: true, module: "esnext", moduleResolution: "bundler",
      resolveJsonModule: true, isolatedModules: true, jsx: "preserve",
      incremental: true, plugins: [{ name: "next" }], paths: { "@/*": ["./*"] },
    },
    include: ["next-env.d.ts", "**/*.ts", "**/*.tsx", ".next/types/**/*.ts"],
    exclude: ["node_modules"],
  }, null, 2));

  // ── Copy components and lib from site-template ──────────────────────────────
  const componentDir = path.join(templateDir, "components");
  if (fs.existsSync(componentDir)) {
    for (const file of fs.readdirSync(componentDir)) {
      copyFile(`components/${file}`, `components/${file}`);
    }
  }
  copyFile("lib/types.ts", "lib/types.ts");

  // ── Shared site config (data module, imported by all pages) ─────────────────
  write("site.config.ts", generateSiteConfig(blueprint));

  // ── CSS with real brand colors and fonts ─────────────────────────────────────
  write("app/globals.css", generateGlobalsCss(blueprint));

  // ── Root layout with JSON-LD, fonts, and site-wide metadata ─────────────────
  write("app/layout.tsx", generateLayout(blueprint));

  // ── Page files (one per route) ───────────────────────────────────────────────
  for (const route of blueprint.frontend.routes) {
    const content = generatePage(route.pageId, route.path, blueprint);
    write(route.file, content);
  }

  // ── Public assets ────────────────────────────────────────────────────────────
  write("public/robots.txt", generateRobotsTxt());
  write("public/sitemap.xml", generateSitemapXml(blueprint));

  // Gallery images use picsum.photos for realistic layout previews.
  // Seeds are deterministic: same seed = same photo on every build.
  // Replace these URLs with real photos before going live.

  return { ok: true, generatedDir, filesWritten, warnings };
}

