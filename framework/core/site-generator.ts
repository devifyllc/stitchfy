// ─── Page generators ──────────────────────────────────────────────────────────

function generatePage(pageId: string, pagePath: string, bp: WebsiteBlueprint): string {
  const generators: Record<string, () => string> = {
    "home": () => generateHomePage(bp),
    "services": () => generateServicesPage(bp),
    "gallery": () => generateGalleryPage(bp),
    "about-us": () => generateAboutPage(bp),
    "contact": () => generateContactPage(bp),
    "book-online": () => generateBookPage(bp),
  };

  return (generators[pageId] ?? (() => generateGenericPage(pageId, pagePath, bp)))();
}

function heroStyleToCssProps(heroStyle: WebsiteBlueprint["ux"]["heroStyle"]): Record<string, string> {
  if (heroStyle.backgroundType === "gradient" && heroStyle.gradientFrom && heroStyle.gradientTo) {
    return {
      background: `linear-gradient(135deg, ${heroStyle.gradientFrom} 0%, ${heroStyle.gradientTo} 100%)`,
      color: heroStyle.textColor,
    };
  }
  if (heroStyle.backgroundType === "solid" && heroStyle.backgroundColor) {
    return {
      backgroundColor: heroStyle.backgroundColor,
      color: heroStyle.textColor,
    };
  }
  // fallback — solid using gradientFrom or textColor inverse
  return { color: heroStyle.textColor };
}

